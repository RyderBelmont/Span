namespace Desktop.Skinning
{
	public enum VisualState
	{
		/// <summary>
		/// Normal state
		/// </summary>
		Normal = 0,
		/// <summary>
		/// Pointer is hovering over element
		/// </summary>
		Hover = 1,
		/// <summary>
		/// Element is being pressed
		/// </summary>
		Pressed = 2,
		/// <summary>
		/// Element is focused
		/// </summary>
		Focused = 3,
		Disabled = 4,
		DisabledSelected = 5,
	}
}
